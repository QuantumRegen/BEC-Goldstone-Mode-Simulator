    "print(\"Max density:\", np.max(density))\n",
    "print(\"Centers separation from fit:\", abs(popt[1] - popt[4]))\n",
    "print(\"Average lobe width (sigma):\", (popt[2] + popt[5])/2)\n",
    "\n",
    "# Plot density + fit\n",
    "plt.plot(x, density, 'b-', label='Density')\n",
